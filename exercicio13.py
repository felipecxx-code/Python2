"""
Um número palíndromo é aquele que lido de trás pra frente é a mesma coisa que lendo de frente pra trás.
Por exemplo, o número 121, 1991, 1001 são palídromos.
Escreva um programa que receba entrada um número inteiro e transforme o mesmo é um número palíndromo juntando ele com o inverso do mesmo. Por exemplo se o usuário der como entrada o número 23 basta juntar 23 com o inverso 32 para formar o número palíndromo 2332. O seu código deve conter comentários para explicar o que está sendo feito.

Dica: Veja que nessa situação é melhor tratar o número como uma str pois é mais fácil de inverter o número dessa maneira. 
"""
valor = input('Digite um valor: ') # Valor que o usuário passar

copia = valor[::-1] # inverte o valor passado pelo usuário
palíndramo = valor + copia # Concatenação do valor passado e invertido
print(palíndramo) # Resultado do palíndromo impresso na tela